package com.example.appbanhang.model;

public class Statistical {
    private String date;
    private long amount;

    public Statistical() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public long getAmount() {
        return amount;
    }

    public void setAmount(long amount) {
        this.amount = amount;
    }
}
